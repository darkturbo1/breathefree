import { serve } from "https://deno.land/std@0.168.0/http/server.ts";

const corsHeaders = {
  'Access-Control-Allow-Origin': '*',
  'Access-Control-Allow-Headers': 'authorization, x-client-info, apikey, content-type',
};

const SYSTEM_PROMPT = `You are a compassionate and knowledgeable smoking cessation coach named "Breathe Coach". Your role is to help users quit smoking by providing:

1. **Emotional Support**: Be empathetic, encouraging, and non-judgmental. Celebrate their progress and remind them of their strength.

2. **Craving Management**: Provide practical, actionable tips for handling cravings (deep breathing, distraction techniques, healthy substitutes).

3. **Health Information**: Share accurate information about the health benefits of quitting and what happens to the body during recovery.

4. **Motivation**: Help users stay focused on their reasons for quitting. Remind them of the money saved, health gained, and life improved.

5. **Relapse Prevention**: If they slip up, never shame them. Help them understand triggers and get back on track immediately.

Key Guidelines:
- Keep responses concise but warm (2-4 paragraphs max)
- Use emojis sparingly for warmth (1-2 per message)
- Always end with an encouraging note or actionable tip
- If asked about non-smoking topics, gently redirect: "I'm here to support your smoke-free journey! Is there anything about quitting smoking I can help you with?"
- Be conversational and personable, not clinical
- Acknowledge that quitting is hard but achievable

Remember: Every message should leave the user feeling supported, capable, and motivated to stay smoke-free.`;

serve(async (req) => {
  // Handle CORS preflight requests
  if (req.method === 'OPTIONS') {
    return new Response(null, { headers: corsHeaders });
  }

  try {
    const { messages } = await req.json();
    const LOVABLE_API_KEY = Deno.env.get("LOVABLE_API_KEY");
    
    if (!LOVABLE_API_KEY) {
      console.error("LOVABLE_API_KEY is not configured");
      throw new Error("LOVABLE_API_KEY is not configured");
    }

    console.log("Processing chat request with", messages.length, "messages");

    const response = await fetch("https://ai.gateway.lovable.dev/v1/chat/completions", {
      method: "POST",
      headers: {
        Authorization: `Bearer ${LOVABLE_API_KEY}`,
        "Content-Type": "application/json",
      },
      body: JSON.stringify({
        model: "google/gemini-2.5-flash",
        messages: [
          { role: "system", content: SYSTEM_PROMPT },
          ...messages,
        ],
        stream: true,
      }),
    });

    if (!response.ok) {
      const errorText = await response.text();
      console.error("AI gateway error:", response.status, errorText);
      
      if (response.status === 429) {
        return new Response(JSON.stringify({ error: "Rate limit exceeded. Please try again in a moment." }), {
          status: 429,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      if (response.status === 402) {
        return new Response(JSON.stringify({ error: "AI credits exhausted. Please add credits to continue." }), {
          status: 402,
          headers: { ...corsHeaders, "Content-Type": "application/json" },
        });
      }
      
      return new Response(JSON.stringify({ error: "Failed to get AI response" }), {
        status: 500,
        headers: { ...corsHeaders, "Content-Type": "application/json" },
      });
    }

    console.log("Streaming response from AI gateway");

    return new Response(response.body, {
      headers: { ...corsHeaders, "Content-Type": "text/event-stream" },
    });
  } catch (error) {
    console.error("Chat error:", error);
    return new Response(JSON.stringify({ error: error instanceof Error ? error.message : "Unknown error" }), {
      status: 500,
      headers: { ...corsHeaders, "Content-Type": "application/json" },
    });
  }
});
